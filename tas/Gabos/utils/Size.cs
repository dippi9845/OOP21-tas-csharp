namespace tas.Gabos.utils
{
    public class Size
    {
        public double Width { get; }

        public double Height { get; }

        public Size(double width, double height)
        {
            Width = width;
            Height = height;
        }

        public Size(int width, int height)
        {
            Width = width;
            Height = height;
        }

    }
}
